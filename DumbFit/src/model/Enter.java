public class Enter {
    
    private String nome;
    private String cpf;
    private String entradaUsu;
    private String saidaUsu;
    
    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getEntradaUsu() {
        return entradaUsu;
    }

    public void setEntradaUsu(String entradaUsu) {
        this.entradaUsu = entradaUsu;
    }

    public String getSaidaUsu() {
        return saidaUsu;
    }

    public void setSaidaUsu(String saidaUsu) {
        this.saidaUsu = saidaUsu;
    }
    
}
